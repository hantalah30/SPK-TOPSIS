window.initBenchmarkDropdown = function(config) {
    const { inputId, scoreInputId, scoreBadgeId, type, data, brands } = config;

    const input = document.getElementById(inputId);
    const scoreInput = document.getElementById(scoreInputId);
    const scoreBadge = document.getElementById(scoreBadgeId);
    if (!input) return;

    const oldDropdown = document.getElementById(inputId + '-dropdown');
    if (oldDropdown) oldDropdown.remove();

    const wrapper = document.createElement('div');
    wrapper.className = 'bench-dropdown';
    wrapper.id = inputId + '-dropdown';
    wrapper.style.cssText = 'position:fixed;background:var(--surface,#fff);border:1px solid var(--border,#e8ecf2);border-radius:10px;box-shadow:0 8px 24px rgba(0,0,0,0.18);z-index:10000;display:none;overflow:hidden;';

    const tabsContainer = document.createElement('div');
    tabsContainer.style.cssText = 'display:flex;gap:4px;padding:8px;border-bottom:1px solid var(--border-light,#f1f4f8);overflow-x:auto;flex-wrap:nowrap;';

    let activeBrand = 'All';
    const brandList = ['All', ...brands];

    brandList.forEach(brand => {
        const tab = document.createElement('button');
        tab.type = 'button';
        tab.textContent = brand;
        tab.style.cssText = 'padding:5px 12px;border-radius:6px;border:none;background:transparent;font-size:11px;font-weight:600;cursor:pointer;color:var(--text-secondary,#64748b);white-space:nowrap;transition:all 0.15s;';
        if (brand === 'All') {
            tab.style.background = 'var(--primary,#4f6ef7)';
            tab.style.color = '#fff';
        }
        tab.onclick = () => {
            activeBrand = brand;
            tabsContainer.querySelectorAll('button').forEach(b => {
                b.style.background = 'transparent';
                b.style.color = 'var(--text-secondary,#64748b)';
            });
            tab.style.background = 'var(--primary,#4f6ef7)';
            tab.style.color = '#fff';
            renderList();
        };
        tabsContainer.appendChild(tab);
    });

    const listContainer = document.createElement('div');
    listContainer.style.cssText = 'overflow-y:auto;';

    function renderList() {
        const search = input.value.toLowerCase().trim();
        const filtered = data.filter(item => {
            const matchBrand = activeBrand === 'All' || item.brand === activeBrand;
            const matchSearch = !search || item.name.toLowerCase().includes(search);
            return matchBrand && matchSearch;
        });

        listContainer.innerHTML = '';

        if (filtered.length === 0) {
            const empty = document.createElement('div');
            empty.style.cssText = 'padding:20px;text-align:center;color:var(--text-muted,#94a3b8);font-size:12px;';
            empty.textContent = 'Tidak ada hasil';
            listContainer.appendChild(empty);
            return;
        }

        filtered.sort((a, b) => a.score - b.score);

        // Limit to 100 items for performance
        const display = filtered.slice(0, 100);

        display.forEach(item => {
            const row = document.createElement('div');
            row.style.cssText = 'display:flex;justify-content:space-between;align-items:center;padding:8px 14px;cursor:pointer;font-size:12px;border-bottom:1px solid var(--border-light,#f1f4f8);transition:background 0.15s;';
            row.onmouseenter = () => row.style.background = 'var(--surface-hover,#f1f5f9)';
            row.onmouseleave = () => row.style.background = 'transparent';
            row.onmousedown = (e) => {
                e.preventDefault();
                selectItem(item);
            };

            const left = document.createElement('div');
            left.style.cssText = 'display:flex;align-items:center;gap:8px;flex:1;min-width:0;';

            const brandTag = document.createElement('span');
            const brandColors = {
                'Intel': '#0071c5', 'AMD': '#ed1c24', 'NVIDIA': '#76b900',
                'Apple': '#888', 'Qualcomm': '#a100ff', 'Cyrix': '#f59e0b',
                '3dfx': '#7c3aed', 'Matrox': '#0891b2', 'S3': '#84cc16',
                'VIA': '#06b6d4', 'IBM': '#1d4ed8', 'IDT': '#db2777',
                'Motorola': '#dc2626', 'MOS Technology': '#9333ea',
                'Zilog': '#ea580c', 'Transmeta': '#65a30d'
            };
            brandTag.style.cssText = `font-size:9px;font-weight:700;padding:2px 6px;border-radius:4px;color:#fff;background:${brandColors[item.brand] || '#888'};letter-spacing:0.3px;`;
            brandTag.textContent = item.brand;

            const name = document.createElement('span');
            name.style.cssText = 'font-weight:500;color:var(--text,#1e293b);overflow:hidden;text-overflow:ellipsis;white-space:nowrap;';
            name.textContent = item.name;

            left.appendChild(brandTag);
            left.appendChild(name);

            const scoreSpan = document.createElement('span');
            scoreSpan.style.cssText = 'font-size:11px;color:var(--text-muted,#94a3b8);font-weight:600;flex-shrink:0;margin-left:8px;';
            scoreSpan.textContent = item.score.toLocaleString();

            row.appendChild(left);
            row.appendChild(scoreSpan);
            listContainer.appendChild(row);
        });

        if (filtered.length > 100) {
            const more = document.createElement('div');
            more.style.cssText = 'padding:8px;text-align:center;color:var(--text-muted,#94a3b8);font-size:11px;font-style:italic;';
            more.textContent = `+${filtered.length - 100} lainnya — ketik untuk filter`;
            listContainer.appendChild(more);
        }
    }

    function selectItem(item) {
        input.value = item.name;
        const skor = window.benchmarkToScore(item.score, type);
        if (scoreInput) scoreInput.value = skor;
        if (scoreBadge) scoreBadge.textContent = skor;
        wrapper.style.display = 'none';
        showInfo(item.score, type);
    }

    function showInfo(score, type) {
        const id = inputId + '-info';
        let info = document.getElementById(id);
        if (!info) {
            info = document.createElement('small');
            info.id = id;
            info.className = 'd-block mt-1';
            info.style.cssText = 'color:#22c55e;font-size:11px;';
            input.parentNode.appendChild(info);
        }
        const source = type === 'processor' ? 'PassMark' : '3DMark';
        info.innerHTML = `<i class="fas fa-check-circle"></i> ${source}: <strong>${score.toLocaleString()}</strong>`;
    }

    function hideInfo() {
        const info = document.getElementById(inputId + '-info');
        if (info) info.remove();
    }

    if (input.parentNode.style.position !== 'relative') {
        input.parentNode.style.position = 'relative';
    }

    wrapper.appendChild(tabsContainer);
    wrapper.appendChild(listContainer);
    document.body.appendChild(wrapper);

    function adjustDropdownPosition() {
        const rect = input.getBoundingClientRect();
        const viewportHeight = window.innerHeight;
        const spaceBelow = viewportHeight - rect.bottom - 16;
        const spaceAbove = rect.top - 16;

        const useUpward = spaceBelow < 240 && spaceAbove > spaceBelow;
        const maxHeight = useUpward
            ? Math.min(360, spaceAbove)
            : Math.min(360, spaceBelow);

        wrapper.style.left = rect.left + 'px';
        wrapper.style.width = rect.width + 'px';

        if (useUpward) {
            wrapper.style.top = (rect.top - maxHeight - 4) + 'px';
        } else {
            wrapper.style.top = (rect.bottom + 4) + 'px';
        }
        wrapper.style.maxHeight = maxHeight + 'px';

        const tabsHeight = tabsContainer.offsetHeight || 50;
        listContainer.style.maxHeight = (maxHeight - tabsHeight - 4) + 'px';
    }

    input.addEventListener('focus', () => {
        wrapper.style.display = 'block';
        adjustDropdownPosition();
        renderList();
    });

    input.addEventListener('input', () => {
        wrapper.style.display = 'block';
        adjustDropdownPosition();
        renderList();
        const found = data.find(d => d.name.toLowerCase() === input.value.toLowerCase().trim());
        if (found) {
            const skor = window.benchmarkToScore(found.score, type);
            if (scoreInput) scoreInput.value = skor;
            if (scoreBadge) scoreBadge.textContent = skor;
            showInfo(found.score, type);
        } else {
            hideInfo();
        }
    });

    window.addEventListener('resize', () => {
        if (wrapper.style.display === 'block') adjustDropdownPosition();
    });
    window.addEventListener('scroll', () => {
        if (wrapper.style.display === 'block') adjustDropdownPosition();
    }, true);

    document.addEventListener('mousedown', (e) => {
        if (!wrapper.contains(e.target) && e.target !== input) {
            wrapper.style.display = 'none';
        }
    });

    const found = data.find(d => d.name.toLowerCase() === input.value.toLowerCase().trim());
    if (found) {
        showInfo(found.score, type);
    }
};

window.setupBenchmarkAutofill = function() {
    document.querySelectorAll('.bench-dropdown').forEach(d => d.remove());

    window.initBenchmarkDropdown({
        inputId: 'inputProcessor',
        scoreInputId: 'skorProc',
        scoreBadgeId: 'skorProcVal',
        type: 'processor',
        data: window.BENCHMARK_DATA.processors,
        brands: ['Intel', 'AMD', 'Apple', 'Qualcomm']
    });
    window.initBenchmarkDropdown({
        inputId: 'inputGpu',
        scoreInputId: 'skorGpu',
        scoreBadgeId: 'skorGpuVal',
        type: 'gpu',
        data: window.BENCHMARK_DATA.gpus,
        brands: ['NVIDIA', 'AMD', 'Intel', 'Apple']
    });
};

document.addEventListener('DOMContentLoaded', function() {
    if (typeof window.setupBenchmarkAutofill === 'function') {
        window.setupBenchmarkAutofill();
    }
});


// Dedupe in case of accidental duplicates (keep first occurrence)
(function dedupeBenchmarks() {
    function dedupe(arr) {
        const seen = new Set();
        return arr.filter(item => {
            const key = item.name.toLowerCase().trim();
            if (seen.has(key)) return false;
            seen.add(key);
            return true;
        });
    }
    window.BENCHMARK_DATA.processors = dedupe(window.BENCHMARK_DATA.processors);
    window.BENCHMARK_DATA.gpus = dedupe(window.BENCHMARK_DATA.gpus);
})();
