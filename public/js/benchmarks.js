// CONVERTER: Benchmark → Skor 1-10 (skala logaritmik)
// =================================================================
const PROC_LOG_MIN = Math.log10(0.1);
const PROC_LOG_MAX = Math.log10(180000);
const GPU_LOG_MIN = Math.log10(1);
const GPU_LOG_MAX = Math.log10(45000);

window.benchmarkToScore = function(value, type) {
    if (!value || value <= 0) return 5;
    const logVal = Math.log10(value);
    let normalized;
    if (type === 'processor') {
        normalized = (logVal - PROC_LOG_MIN) / (PROC_LOG_MAX - PROC_LOG_MIN);
    } else {
        normalized = (logVal - GPU_LOG_MIN) / (GPU_LOG_MAX - GPU_LOG_MIN);
    }
    return Math.max(1, Math.min(10, Math.round(normalized * 9 + 1)));
};

